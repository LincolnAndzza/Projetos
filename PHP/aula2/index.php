		<h2 style="color: red">Rainha Maeve</h2>
			<p align="justify"><font face="calibri" size="3" style="color: white">É uma das personagens principais da série Amazon The Boys. Ela é uma super-heroína e membro dos Sete.
			Como guerreira, feminista, humanitária e o segundo membro mais poderoso dos Sete, a Rainha Maeve é ​​o modelo definitivo para meninas em todo o mundo. Mas para alcançar alturas tão surpreendentes, ela teve que fazer sacrifícios ao longo do caminho. Abraçar a imagem que Vought exigia que ela aceitasse significava abandonar as verdades e até mesmo algumas das pessoas que ela amava.</font></p>

	</br>

		<h2 style="color: blue">Billy Butcher</h2>
			<p align="justify"><font face="calibri" size="3" style="color: white">Ex-membro das forças especiais britânicas que se tornou vigilante, Billy Butcher é tão charmoso quanto astuto. Ele é uma força da natureza, que pode convencer quase qualquer pessoa a fazer qualquer coisa, seja através de um sorriso ou força bruta ou às vezes ambos. Ele é consumido por uma missão na vida: destruir super-heróis. Mas essa vingança pessoal é motivada por seu ódio por um Supe em particular: Homelander. Butcher está determinado a se vingar de Homelander, custe o que custar. E ele não vai deixar ninguém ou nada ficar em seu caminho.</font></p>
